    private void btnModifyStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyStudentActionPerformed
        for (int i = 0; i < listStudents.size(); i++) {
            if (studentsCombo.getSelectedItem().toString().equals(listStudents.get(i).getName())) {
                listStudents.get(i).setName(fieldNameStudent.getText());
                listStudents.get(i).setEmail(fieldEmailStudent.getText());
                posi = studentsCombo.getSelectedIndex();
                studentsCombo.insertItemAt(fieldNameStudent.getText(), posi);
                studentsCombo.removeItemAt(posi + 1);
                studentsCombo.setSelectedIndex(posi);

                studentLessonsCombo.insertItemAt(fieldNameStudent.getText(), posi);
                studentLessonsCombo.removeItemAt(posi + 1);
                studentLessonsCombo.setSelectedIndex(posi);

                studentQuestionsCombo.insertItemAt(fieldNameStudent.getText(), posi);
                studentQuestionsCombo.removeItemAt(posi + 1);
                studentQuestionsCombo.setSelectedIndex(posi);

                JOptionPane.showMessageDialog(null, "Student Modified");
            }
        }
    }//GEN-LAST:event_btnModifyStudentActionPerformed

    private void btnModifyLessonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyLessonActionPerformed
        for (int i = 0; i < listStudents.size(); i++) {
            if (studentLessonsCombo.getSelectedItem().toString().equals(listStudents.get(i).getName())) {
                student = listStudents.get(i);
                for (int p = 0; p < student.listLesson.size(); p++) {
                    if (lessonsCombo.getSelectedItem().toString().equals(student.listLesson.get(p).getName())) {
                        student.listLesson.get(p).setName(fieldNameLesson.getText());
                        student.listLesson.get(p).setDescription(fieldDescriptionLesson.getText());
                        student.listLesson.get(p).setMaterial(sm);

                        posi = lessonsCombo.getSelectedIndex();
                        lessonsCombo.insertItemAt(fieldNameLesson.getText(), posi);
                        lessonsCombo.removeItemAt(posi + 1);
                        lessonsCombo.setSelectedIndex(posi);
                        
                        JOptionPane.showMessageDialog(null, "Lesson Modified");
                    }
                }
            }
        }
    }//GEN-LAST:event_btnModifyLessonActionPerformed

