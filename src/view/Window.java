    private void btnAddQuestionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddQuestionActionPerformed
        question = new Question();
        for (int i = 0; i < listStudents.size(); i++) {
            if (studentQuestionsCombo.getSelectedItem().toString().equals(listStudents.get(i).getName())) {
                student = listStudents.get(i);
                for (int p = 0; p < student.listLesson.size(); p++) {
                    if (lessonsQuestionsCombo.getSelectedItem().toString().equals(student.listLesson.get(p).getName())) {
                        lesson = student.listLesson.get(p);
                        question.addAnswer(fieldQuestion2.getText());
                        question.addAnswer(fieldQuestion3.getText());
                        question.addAnswer(fieldQuestion4.getText());
                        question = admin.createQuestion(statementQuestion.getText(), fieldCorrectAnswer.getText());
                        lesson.listQuestions.add(question);
                        JOptionPane.showMessageDialog(null, "Question Added");
                    }
                }
            }
        }
    }//GEN-LAST:event_btnAddQuestionActionPerformed

